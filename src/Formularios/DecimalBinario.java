      public static int Decimal(long numero){
    int decimalNumber=0,i=0;
    long tam;
    while (numero!=0){
      tam=numero%10;
      numero/=10;
      decimalNumber+=tam*Math.pow(2,i);
      i++;
      
    }
    return decimalNumber;
  }
